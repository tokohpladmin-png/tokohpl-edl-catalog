function normalize(value: string) {
  return value
    .toLowerCase()
    .trim()
    .replace(/['"]/g, '')
    .replace(/[^a-z0-9]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function compactCode(value: string) {
  return normalize(value).replace(/\s+/g, '');
}

const detailsByCode = new Map(
  edlProductDetails
    .filter((detail) => detail.code)
    .map((detail) => [compactCode(detail.code), detail])
);

const detailsByName = new Map(
  edlProductDetails.map((detail) => [normalize(detail.itemName), detail])
);

export function findEdlProductDetail(input: { code?: string; name?: string; sku?: string }) {
  const codeCandidates = [input.code, input.sku, input.name].filter(Boolean) as string[];

  for (const candidate of codeCandidates) {
    const normalized = compactCode(candidate);
    const direct = detailsByCode.get(normalized);
    if (direct) return direct;

    for (const [key, detail] of Array.from(detailsByCode.entries())) {
      if (normalized.includes(key) || key.includes(normalized)) {
        return detail;
      }
    }
  }

  if (input.name) {
    const normalizedName = normalize(input.name);
    const directName = detailsByName.get(normalizedName);
    if (directName) return directName;

    for (const detail of edlProductDetails) {
      const detailName = normalize(detail.itemName);
      if (normalizedName.includes(detailName) || detailName.includes(normalizedName)) {
        return detail;
      }
    }
  }

  return null;
}
